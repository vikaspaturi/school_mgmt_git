    window.onerror=function(){ return true; }
    var slideTimer,slideTimer2;
    var slidesCount=slidesCount2=0;
    var presentSlide=presentSlide2=0;
    $(function(){
        sliderInit();
        setTimeout(sliderInit2, 1000);
        $('#accordion_div').accordion({
                collapsible: true
        });
    });

    function sliderInit(){
        slidesCount=$('.slide_item').length;
        $('.slide_item:eq(0)').fadeIn();
        slideTimer=setTimeout(sliderChange, 3000);
        slideButtons='';
        for(i=0;i<slidesCount;i++){
            slideButtons+='<a href="javascript:void(0);" style="padding:5px 3px;" onclick="showSlide('+i+')">*</a>';
        }
        $('.slide_buttons').html(slideButtons);
    }

    function sliderChange(){
        $('.slide_item:eq('+presentSlide+')').slideUp(function(){
            $(this).hide(); // For IE
            (presentSlide+1==slidesCount)?presentSlide=0:presentSlide++;
            $('.slide_item:eq('+presentSlide+')').slideDown();
        });
        clearTimeout(slideTimer);
        slideTimer=setTimeout(sliderChange, 3000);
    }
    function showSlide(i){
        presentSlide=i;
        clearTimeout(slideTimer);
        slideTimer=setTimeout(sliderChange, 7000);
        $('.slide_item').hide().fadeOut('fast',function(){
            $('.slide_item:eq('+i+')').fadeIn('fast');
        });
    }




    function sliderInit2(){
        slidesCount2=$('.slide_item2').length;
        $('.slide_item2:eq(0)').fadeIn();
        slideTimer2=setTimeout(sliderChange2, 4000);
    }

    function sliderChange2(){
        $('.slide_item2:eq('+presentSlide2+')').fadeOut(function(){
            (presentSlide2+1==slidesCount2)?presentSlide2=0:presentSlide2++;
            $('.slide_item2:eq('+presentSlide2+')').fadeIn();
        });
        clearTimeout(slideTimer2);
        slideTimer2=setTimeout(sliderChange2, 4000);
    }
</script>